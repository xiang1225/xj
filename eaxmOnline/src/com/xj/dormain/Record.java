package com.xurui.dormain;

public class Record {
	private String Uid;
	private String course;
	private float score;
	private float single;
	private float multiply;
	private float judge;
	private float jd;
	private float program;
	private String test_time;
	private int count;

	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getUid() {
		return Uid;
	}
	public void setUid(String uid) {
		Uid = uid;
	}
	public String getCourse() {
		return course;
	}
	public void setCourse(String course) {
		this.course = course;
	}
	public float getScore() {
		return score;
	}
	public void setScore(float score) {
		this.score = score;
	}
	public float getSingle() {
		return single;
	}
	public void setSingle(float single) {
		this.single = single;
	}
	public float getMultiply() {
		return multiply;
	}
	public void setMultiply(float multiply) {
		this.multiply = multiply;
	}
	public float getJudge() {
		return judge;
	}
	public void setJudge(float judge) {
		this.judge = judge;
	}
	public float getJd() {
		return jd;
	}
	public void setJd(float jd) {
		this.jd = jd;
	}
	public float getProgram() {
		return program;
	}
	public void setProgram(float program) {
		this.program = program;
	}
	public String getTest_time() {
		return test_time;
	}
	public void setTest_time(String test_time) {
		this.test_time = test_time;
	}


}
